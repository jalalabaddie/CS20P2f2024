package SkillBuilders;
import java.util.*;

public class Evens
{
	public static void main(String[] args)
	{
		
		// Prompting message declaring the even numbers between 1 and 20.
		 System.out.println("Here are the even values of numbers from 1 to 20:");

	        // Cycle through the digits 2 through 20, adding two each time.
	        for (int evens = 2; evens <= 20; evens += 2) 
	        {
	            System.out.println(evens);
	        }
	    }
	}
		
	
	
	
	

